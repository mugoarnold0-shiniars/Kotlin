fun main(){
    // While loop
    // You initialize a variable with an initial value
    // while keyword, condition to be evaluated ,code to be executed if condition is true,increment/decrement
    // val - It dictates a constant value i.e :cannot change
    // var - it dictates a values that can change overtime..
    var count = 0
    while (count <=10){
        println("It is looping: $count")
        count=count+1
    }
}